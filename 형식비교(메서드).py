# List 예제
my_list = [1, 2, 3, 4, 5]

# Tuple 예제
my_tuple = (1, 2, 3, 4, 5)

# Set 예제
my_set = {1, 2, 3, 4, 5}

# Dictionary 예제
my_dict = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5}

# 각 자료형의 메서드를 사용하여 비교
# List 메서드 사용
print("List의 길이:", len(my_list))
print("List의 합:", sum(my_list))

# Tuple 메서드 사용
print("Tuple의 길이:", len(my_tuple))
print("Tuple의 합:", sum(my_tuple))

# Set 메서드 사용
print("Set의 길이:", len(my_set))

# Dictionary 메서드 사용
print("Dictionary의 키 목록:", my_dict.keys())
print("Dictionary의 값 목록:", my_dict.values())
